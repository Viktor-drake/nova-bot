async function chatAnketa(history, userMessage) {
  return converse(SYSTEM_PROMPT, history, userMessage, {
    model: ANKETA_MODEL,
    maxTokens: 1200,
  });
}

async function extractFromDialog(history) {
  // history: array of {role, content}
  const dialogText = history
    .map((m) => `${m.role === "user" ? "ПОЛЬЗОВАТЕЛЬ" : "АССИСТЕНТ"}: ${m.content}`)
    .join("\n\n");

  const raw = await chat([
    { role: "system", content: EXTRACTION_PROMPT },
    { role: "user", content: `Диалог анкеты:\n\n${dialogText}\n\nВерни JSON.` },
  ], { model: ANKETA_MODEL, maxTokens: 4000 });

  // strip code fences if any
  const cleaned = raw.replace(/^```(?:json)?\s*/i, "").replace(/\s*```\s*$/i, "").trim();
  try {
    return JSON.parse(cleaned);
  } catch (e) {
    // try to find first { ... last }
    const start = cleaned.indexOf("{");
    const end = cleaned.lastIndexOf("}");
    if (start >= 0 && end > start) {
      return JSON.parse(cleaned.slice(start, end + 1));
    }
    throw new Error(`Anketa extraction failed to parse JSON: ${e.message}`);
  }
}

